int size = 25;
int main()
{
    int arr[size];
    int i,n,count=0;
   // printf("Enter Elements of array");
    for (i = 0; i < size; i++)
    {                                               //for input
        printf("Enter %d element\n",i);
        scanf("%d",&arr[i]);
    }
    printf("Enter Number to be searched-\n");
    scanf("%d",&n);
    for (i = 0; i < size; i++)
    {                                               //for output
       if(arr[i]==n)
       {
           count++;
       }
    }
    if (count=!0)
    {
        printf("Number %d found %d times",n,count);
    }
    
    return 0;
}
